package Day_13_ForLoop;

public class _01_forLoop {
    public static void main(String[] args) {

        for ( int counter = 0; counter < 5; counter++){
            System.out.println("For loop " + counter);
        }
        System.out.println();

        int counter2 = 0;
        while (counter2 < 5){
            System.out.println("While loop " + counter2);
            counter2++;
        }
    }
}
