package TubaMentoring;

public class Task2 {
     /*
        create one char which is a

        print all the char from a - z

        in the while loop

        Create the alphabet
         */
     public static void main(String[] args) {
         char firstLetter = 'a';
         while (firstLetter <= 'z'){
             System.out.print(firstLetter + " ");
             firstLetter++;
         }
     }
}
